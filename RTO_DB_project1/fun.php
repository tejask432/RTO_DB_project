<h1 class="subhead-blue"><u>Divisional Level</u></h1>

<p>At the divisional level the <strong>Joint Commissioner for Transport </strong>supervises and guides the Regional Transport Officers and Assistant Regional Transport Ofﬁcers coming within jurisdiction in the day-to-day administration. They also conduct cursory and detailed inspection of the offices coming under their control periodically. Further the <strong>Joint Commissioner for Transport</strong> also functions as appellate authorities against the orders of the Regional Transport Officers and Assistant Regional Transport Officers under the above said Act and rules in respect of suspension of Driving/ Conductor license, refusal to registration of vehicles, rejection of renewal of certificate of registration of Non» Transport vehicles, refusal to issue and renewal of Fitness certificate, suspension/cancellation of Fitness certificates and Tax matters.</p>

<p>The <strong>Regional Transport Officers</strong> and the <strong>Assistant Regional Transport Officers</strong> are the heads of the Regional and sub&nbsp;regional offices respectively. The Regional Transport Officer of the respective headquarters are functioning as licensing authorities for issue of Driving or Conductors licenses' Registration Authorities for Registration of Motor Vehicles. They also act as Secretaries of Regional Transport Authorities for the grant of permits for Transport Vehicles.</p>

<p><strong>Senior Inspector of Motor Vehicle </strong>and<strong> Inspector of Motor Vehicle</strong>- these executive staff has to carry the work as listed under Section 213(5) of the Motor Vehicles Act, 1988 and Rule 259 (2) of the Karnataka Motor Vehicles Rules, 1989.</p>

<p><strong>Superintendents</strong> — They have to supervise the work of the Subordinate staff in the office in particular section assigned to them. No independent powers except as taxation authority.</p>

<p>The other supportive staff viz., Stenographers. First Division Assistants, Second Division Assistants, Typists, Group-D have to carryout the instructions of the Head of the Office. They have independent powers.</p>

