<h1 class="subhead-blue"><u><strong>I (A) Details of Organizational set up, Functions and Duties of the Department.</strong></u></h1>

<p>Transport Department is one of the major department of the state.The Transport Department is functioning in the State through its unit Offices i.e., Regional Transport Offices and Assistant Regional Transport Offices with its Head Quarter at Bengaluru and situated at 1st Floor, TTMC. Building, Shanthi nagar, Bengaluru.</p>

<p>The organizational set up of the Transport Department is given below:</p>

<p>The department is headed by the Commissioner for Transport as the Head of the Department and is assisted by the following ofﬁcers at the Head Quarters.</p>

<ol>
	<li>Additional&nbsp;Commissioner for Transport (Administration).</li>
	<li>Additional Commissioner for Transport (Enforcement South).</li>
	<li>Additional Commissioner for Transport and secretary, Karnataka State Transport Authority, Bengaluru.</li>
	<li>Joint Commissioner for Transport (Environment and E-Governance).</li>
	<li>The Law Ofﬁcer.</li>
	<li>The Financial Assistant (Accounts).</li>
	<li>The Financial Assistant (Audit).</li>
	<li>The Assistant Director of Statistics.</li>
	<li>Two Assistant Law Ofﬁcers.</li>
	<li>Four Assistant Secretaries.</li>
</ol>

<p>The Commissioner for Transport is also assisted by the Additional Commissioner for Transport (Enforcement North Zone). Dharwad, Joint Commissioner for Transport (Urban and Rural). Bengaluru and respective Deputy Commissioners for Transport, Regional Transport Ofﬁcers and Assistant Regional Transport Ofﬁcers who are the head of Zonal, Divisional, Regional and Sub—Regional ofﬁcers respectively.</p>

<p>The Transport Department is mainly involved in the task of the following functions and duties.</p>

<ol>
	<li>Registration of Motor vehicle</li>
	<li>Issue of driving and conductor licenses</li>
	<li>Issue of permits for goods and Passenger vehicles</li>
	<li>Collection of Motor vehicle taxes</li>
	<li>Enforcement of Motor vehicle acts and rules</li>
	<li>Road safety measures</li>
	<li>Vehicular pollution control measures</li>
</ol>

<h1 class="subhead-blue"><u><strong>II. Powers and Duties of Ofﬁcers and Employees</strong></u></h1>

<p>The Commissioner for Transport is the Head of the Transport Department. The Commissioner for Transport takes decisions on important matters concerning the department and attends all the meetings at the government level related to the important issues of the department. The Commissioner for Transport exercises overall control of the department. He is also the Appointing and Disciplinary Authority in respect of Group&nbsp;C and, D of the Department. He also exercises the powers as per the various provisions of the Act and Rules pertaining to the Department, The Commissioner for Transport is also the Chairman of the Karnataka State Transport Authority.</p>

<p>The Commissioner for Transport is also assisted by the Addl. Commissioner for Transport (Enforcement North Zone), Dharwad, Joint Commissioner for Transport (Urban and&nbsp;Rural) Bengaluru and respective Deputy Commissioners for Transport, Regional Transport Ofﬁcers and Assistant Regional Transport Ofﬁcers who are head of Zonal, Divisional, Regional and Sub&nbsp;Regional Ofﬁcers respectively.</p>

